import Foundation
import UIKit

extension UIColor {
    static var midnightBlue: UIColor {
        return UIColor(named: "midnightBlue") ?? .darkGray
    }
    
    static var pomegranate: UIColor {
        return UIColor(named: "pomegranate") ?? .systemRed
    }
    
    static var peterRive: UIColor {
        return UIColor(named: "peterRiver") ?? .systemBlue
    }
}
